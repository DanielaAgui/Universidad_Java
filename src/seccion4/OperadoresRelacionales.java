package seccion4;

public class OperadoresRelacionales {
    public static void main(String[] args) {
        int a = 3;
        int b = 2;

        //Mayor o igual que
        boolean c = (a >= b);
        System.out.println(c);

        //Mayor que
        boolean d = a > b;
        System.out.println(d);

        //Menor que
        boolean e = a < b;
        System.out.println(e);

        //Menor o igual que
        boolean f = a <= b;
        System.out.println(f);


    }
}
