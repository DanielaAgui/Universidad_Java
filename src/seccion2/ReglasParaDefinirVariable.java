        //Primer letra de la primera palabra en minuscula
        //Si lleva varias palabras, la primer letra en mayuscula
        //No puede comenzar con numeros ni caracteres especiales
        //Puede empezar con guion bajo o signo pesos | _ | $ |
        //No se recomienda utilizar tildes
